function confirmacionSuscribirse() {
    var respuesta = confirm("Estas seguro que desea suscribirse?");
    if (respuesta == true) {
        return true;   
    } else {
        return false;
    }
}

function confirmacion() {
    var respuesta = confirm("Estas seguro que desea enviar esta informacion?");
    if (respuesta == true) {
        return true;   
    } else {
        return false;
    }
}

